const web3 = new Web3(Web3.givenProvider || "ws://localhost:8545");
const myContract = new web3.eth.Contract(contractABI, contractAddress);

document.getElementById('buyButton').addEventListener('click', function() {
    document.getElementById('popup').classList.remove('hidden');
    
    // Pop-up açıldığında varsayılan olarak seçili olan ülkenin fiyatını güncelle
    const country = document.getElementById('countrySelect').value;
    updatePrice(country);
});

document.getElementById('closePopup').addEventListener('click', function() {
    document.getElementById('popup').classList.add('hidden');
});

document.getElementById('countrySelect').addEventListener('change', function() {
    const country = this.value;
    updatePrice(country);
});

document.getElementById('mintButton').addEventListener('click', async function() {
    const accounts = await web3.eth.requestAccounts();
    const userAccount = accounts[0];

    // Fiyatı güncelle
    const totalPrice = document.getElementById('totalPrice').innerText;
    const totalPriceInWei = web3.utils.toWei(totalPrice, 'ether');

    console.log("Toplam fiyat (ETH):", totalPrice);
    console.log("Toplam fiyat (Wei):", totalPriceInWei);

    const country = document.getElementById('countrySelect').value;

    // Kontrata mint işlemi gönder
    myContract.methods.mintNFT(country)
        .send({ from: userAccount, value: totalPriceInWei })
        .on('receipt', function(receipt) {
            console.log('NFT successfully minted!', receipt);
            alert('Mint işlemi başarılı. Transaction Hash: ' + receipt.transactionHash);
        })
        .on('error', function(error) {
            console.error('Error minting NFT:', error);
            alert('Mint işlemi başarısız: ' + error.message);
        });
});

// Fiyat güncelleme fonksiyonu
function updatePrice(country) {
    let shippingCost;

    switch(country) {
        case 'USA':
            shippingCost = 0.20; // ETH olarak kargo ücretleri
            break;
        case 'CANADA':
            shippingCost = 0.25;
            break;
        case 'UK':
            shippingCost = 0.30;
            break;
        case 'GERMANY':
            shippingCost = 0.35;
            break;
        case 'FRANCE':
            shippingCost = 0.28;
            break;
        case 'JAPAN':
            shippingCost = 0.40;
            break;
        case 'AUSTRALIA':
            shippingCost = 0.45;
            break;
        case 'BRAZIL':
            shippingCost = 0.38;
            break;
        case 'INDIA':
            shippingCost = 0.32;
            break;
        case 'CHINA':
            shippingCost = 0.37;
            break;
        default:
            shippingCost = 0;
    }

    const productPrice = 1; // Ürün fiyatı sabit 1 ETH
    const totalPrice = productPrice + shippingCost;
    document.getElementById('totalPrice').innerText = totalPrice.toString();

    console.log("Seçilen ülke:", country);
    console.log("Toplam fiyat (ETH):", totalPrice);
    console.log("Toplam fiyat (Wei):", web3.utils.toWei(totalPrice.toString(), 'ether'));
}
